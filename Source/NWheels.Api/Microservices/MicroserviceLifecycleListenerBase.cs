using System;

namespace NWheels.Api.Microservices
{
    public abstract class MicroserviceLifecycleListenerBase : IMicroserviceLifecycleListener
    {
        private readonly string _idName;

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        protected MicroserviceLifecycleListenerBase(string idName)
        {
            _idName = idName;
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Loading()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Load()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Loaded()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Activating()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Activate()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void Activated()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightBeDeactivating()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightDeactivate()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightBeDeactivated()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightBeUnloading()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightUnload()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public virtual void MightBeUnloaded()
        {
        }

        //-----------------------------------------------------------------------------------------------------------------------------------------------------

        public string IdName
        {
            get
            {
                return _idName;
            }
        }
    }
}
